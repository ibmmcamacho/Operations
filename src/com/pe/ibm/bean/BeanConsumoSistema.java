package com.pe.ibm.bean;

public class BeanConsumoSistema {
	
	String estado;
	String cantidad;
	String porcent;
	String umbral;
	String valmax;
	String reporte;
	String hora;
	String fecha;
	
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCantidad() {
		return cantidad;
	}
	public void setCantidad(String cantidad) {
		this.cantidad = cantidad;
	}
	public String getPorcent() {
		return porcent;
	}
	public void setPorcent(String porcent) {
		this.porcent = porcent;
	}
	public String getUmbral() {
		return umbral;
	}
	public void setUmbral(String umbral) {
		this.umbral = umbral;
	}
	public String getValmax() {
		return valmax;
	}
	public void setValmax(String valmax) {
		this.valmax = valmax;
	}
	public String getReporte() {
		return reporte;
	}
	public void setReporte(String reporte) {
		this.reporte = reporte;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	
	
	
}
